    //Untuk memastikan kalau yang dimasukkan adalah Nomor dalam Text Box Durasi
    private void txtDurasiKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDurasiKeyPressed
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(Character.isLetter(c)){
            txtDurasi.setEditable(false);
            JOptionPane.showMessageDialog(this, "Masukkan Nomor yang valid",
                                   "ERROR", JOptionPane.ERROR_MESSAGE);
        }else
            txtDurasi.setEditable(true);
    }//GEN-LAST:event_txtDurasiKeyPressed

