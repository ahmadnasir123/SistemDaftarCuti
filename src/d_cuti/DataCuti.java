    //Button Simpan when clicked
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String id = txtId.getText();
        String nama = txtNama.getText();
        String jenis = (String)cbJenis.getSelectedItem();
        String durasi = txtDurasi.getText();
        String alasan = txtbxAlasan.getText();
        String status = (String)cbStatus.getSelectedItem();
        
        //Nested Try Catch Button Simpan
        try{
            Connection c = Connector_db.getKoneksi();
            String sql = "INSERT INTO daftar_cuti VALUES (?, ?, ?, ?, ?, ?)";
            try (PreparedStatement p = c.prepareStatement(sql)) {
                p.setString(1, id);
                p.setString(2, nama);
                p.setString(3, jenis);
                p.setString(4, durasi);
                p.setString(5, alasan);
                p.setString(6, status);
                p.executeUpdate();
            }
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        }catch(SQLException e){
            System.out.println("Error : Data Tidak Tersimpan");
            System.exit(1);
        }finally{
            loadData();
            generate_id();
            clear();
        }
    }//GEN-LAST:event_btnTambahActionPerformed
    
    //Button Edit when clicked
    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if(i == -1){
            return;
        }
        String id = (String)tb_model.getValueAt(i, 0);
        String nama = txtNama.getText();
        String jenis = (String)cbJenis.getSelectedItem();
        String durasi = txtDurasi.getText();
        String alasan = txtbxAlasan.getText();
        String status = (String)cbStatus.getSelectedItem();
        
        //Nested Try Catch Button Edit
        try{
            Connection c = Connector_db.getKoneksi();
            String sql = "UPDATE daftar_cuti SET nama = ?, jenis = ?, durasi = ?, alasan = ?, status = ? WHERE id = ?";
            try (PreparedStatement p = c.prepareStatement(sql)) {
                p.setString(1, nama);
                p.setString(2, jenis);
                p.setString(3, durasi);
                p.setString(4, alasan);
                p.setString(5, status);
                p.setString(6, id);
                p.executeUpdate();
            }
            JOptionPane.showMessageDialog(null, "Edit Berhasil");
            btnTambah.setEnabled(true);
            cbStatus.setEnabled(false);
            clear();
        }catch(SQLException e){
            System.out.println("Error : Edit Error");
            System.exit(1);
        }finally{
            loadData();
            generate_id();
        }
    }//GEN-LAST:event_btnEditActionPerformed

    //Button Hapus when clicked
    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        cbStatus.setEnabled(false);
        clear();
        
        int i = jTable1.getSelectedRow();
        if(i == -1){
            return;
        }
        
        String id = (String)tb_model.getValueAt(i, 0);
        
        int question = JOptionPane.showConfirmDialog(null, "Apakah anda ingin menghapus data ?","Hapus", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if(question == JOptionPane.OK_OPTION){
            //Nested Try catch button hapus
            try{
                Connection c = Connector_db.getKoneksi();
                String sql = "DELETE FROM daftar_cuti WHERE id = ?";
                try (PreparedStatement p = c.prepareStatement(sql)) {
                    p.setString(1, id);
                    p.executeUpdate();
                }
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            }catch(SQLException e){
                System.out.println("Error : Data Tidak Terhapus");
                System.exit(1);
            }finally{
                loadData();
                generate_id();
                clear();
            }
        }
        if(question == JOptionPane.CANCEL_OPTION){
        }
    }//GEN-LAST:event_btnHapusActionPerformed
    
