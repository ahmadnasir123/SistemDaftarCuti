    //When trying to search 
    private void txtSearchKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyTyped
        // TODO add your handling code here:
        DefaultTableModel tabel = new DefaultTableModel();
        
        tabel.addColumn("ID");
        tabel.addColumn("Nama");
        tabel.addColumn("Jenis");
        tabel.addColumn("Durasi");
        tabel.addColumn("Alasan Cuti");
        tabel.addColumn("Status");
        
        //Nested Try Catch search
        try{
            Connection c = Connector_db.getKoneksi();
            String sql = "Select * from daftar_cuti where nama like '%" + txtSearch.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while(rs.next()){
                tabel.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getInt(4),
                    rs.getString(5),
                    rs.getString(6),
                });
            }
            jTable1.setModel(tabel);
            loadData();
        }catch(SQLException e){
           System.out.println("Error : Terjadi Kesalahan Dalam Search");
           System.exit(1);
        }finally{
            loadData();
        }
    }//GEN-LAST:event_txtSearchKeyTyped
    
