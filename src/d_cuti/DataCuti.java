    //Private AutoNumber for ID
    private void generate_id(){
        try{
            Connection c = Connector_db.getKoneksi();
            try (Statement s = c.createStatement()) {
                String sql = "SELECT * FROM daftar_cuti ORDER BY id DESC";
                try (ResultSet r = s.executeQuery(sql)) {
                    if (r.next()){
                        String NoTiket = r.getString("id").substring(5);
                        String TK = "" + (Integer.parseInt(NoTiket) + 1);
                        String Nol = "";
                        
                        switch (TK.length()) {
                            case 1:
                                Nol = "00000";
                                break;
                            case 2:
                                Nol = "0000";
                                break;
                            case 3:
                                Nol = "";
                                break;
                            default:
                                break;
                        }
                        
                        txtId.setText("TK" + Nol + TK);
                        
                    }else{
                        txtId.setText("TK000001");
                    }
                }
            }
        }catch(NumberFormatException | SQLException e){
            System.out.println("Error : ID Tiket Gagal Generate");
            System.exit(1);
        }
    }
    
