    //Load data to table
    public void loadData(){
        tb_model.getDataVector().removeAllElements();
        tb_model.fireTableDataChanged();
        
        //Try Catch load data
        try{
            Connection c = Connector_db.getKoneksi();
            try (Statement s = c.createStatement()) {
                String sql = "SELECT * FROM daftar_cuti";
                //While loop
                try (ResultSet r = s.executeQuery(sql)) {
                    //While loop
                    while(r.next()){
                        //Array Load data
                        Object[] arrayData = new Object[6];
                        arrayData [0] = r.getString("id");
                        arrayData [1] = r.getString("nama");
                        arrayData [2] = r.getString("jenis");
                        arrayData [3] = r.getString("durasi");
                        arrayData [4] = r.getString("alasan");
                        arrayData [5] = r.getString("status");
                        tb_model.addRow(arrayData);
                    }
                }
            }
        }catch(SQLException e){
            System.out.println("Error : Tidak Berhasil Load Data");
            System.exit(1);
        }
    }
    
    
